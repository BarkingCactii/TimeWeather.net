                
                <p align="center">
                <font size="1">
                WorldTIme2003 is a skinnable world 
                time viewing program containing time information for over 1,200 
                cities. Each skin contains the rules about what information is 
                to be displayed and formatted using a template. No messing 
                around changing settings because you loaded a new skin. Multiple 
                alarms can be set for each location and can be set to recur. 
                Alarms can be set to display a text message in a pop up window, 
                run an application, or play a sound. It can be set up to display 
                contact information rather than geographical information. Update 
                to the latest skins via the internet automatically. Includes 
                system tray support.</font></p>
